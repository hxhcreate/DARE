"""
Preprocess the ultrafeedback dataset to parquet format
"""

import argparse
import os

import datasets

from verl.utils.hdfs_io import copy, makedirs


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--local_dir", default=None, help="The save directory for the preprocessed dataset.")
    parser.add_argument("--hdfs_dir", default=None)
    parser.add_argument("--local_dataset_path", default=None, help="The local path to the raw dataset, if it exists.")
    parser.add_argument(
        "--local_save_dir", default="~/data/preprocessed/dpo", help="The save directory for the preprocessed dataset."
    )

    args = parser.parse_args()
    local_dataset_path = args.local_dataset_path

    data_source = "argilla/ultrafeedback-binarized-preferences-cleaned"

    if local_dataset_path is not None:
        dataset = datasets.load_dataset(local_dataset_path, "default")
    else:
        dataset = datasets.load_dataset(data_source, "default")

    split_dataset = dataset['train'].train_test_split(test_size=0.05, seed=42)    

    train_dataset = split_dataset["train"]
    test_dataset = split_dataset["test"]

    
    # add a row to each data item that represents a unique id
    def make_map_fn(split):
        def process_fn(example, idx):
            prompt = example.pop('prompt')
            if not isinstance(prompt, list):
                prompt = [{
                    "role": "user",
                    "content": prompt
                }]
                
            chosen = example.pop('chosen')[:1]
            rejected = example.pop('rejected')[:1]
            
            data_source = example.pop('source', 'unknown')
            data = {
                "data_source": data_source,
                "prompt": prompt,
                "chosen": chosen,
                "rejected": rejected,
                "extra_info": {
                    'split': split,
                    'index': idx
                }
            }
            return data

        return process_fn

    train_dataset = train_dataset.map(function=make_map_fn("train"), with_indices=True)
    test_dataset = test_dataset.map(function=make_map_fn("test"), with_indices=True)

    hdfs_dir = args.hdfs_dir
    local_save_dir = args.local_dir
    if local_save_dir is not None:
        print("Warning: Argument 'local_dir' is deprecated. Please use 'local_save_dir' instead.")
    else:
        local_save_dir = args.local_save_dir
    
    os.makedirs(local_save_dir, exist_ok=True)
    os.makedirs(os.path.join(local_save_dir, "train"), exist_ok=True)
    os.makedirs(os.path.join(local_save_dir, "test"), exist_ok=True)

    train_dataset.to_parquet(os.path.join(local_save_dir, "train", "ultrafeedback.parquet"))
    test_dataset.to_parquet(os.path.join(local_save_dir, "test",  "ultrafeedback.parquet"))

    if hdfs_dir is not None:
        makedirs(hdfs_dir)

        copy(src=local_save_dir, dst=hdfs_dir)
